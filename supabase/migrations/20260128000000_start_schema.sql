CREATE OR REPLACE FUNCTION "public"."admin_clear_slot"("slot_id" "uuid") RETURNS "void"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
DECLARE
  current_uid UUID;
  is_admin BOOLEAN;
BEGIN
  -- 1. Get User ID
  current_uid := auth.uid();
  
  -- 2. Check Admin Status (Simplified & Robust)
  SELECT EXISTS (
    SELECT 1 FROM public.profiles 
    WHERE id = current_uid 
    AND role = 'admin'
  ) INTO is_admin;
  
  -- Fallback for specific Admin if needed
  IF current_uid = '43250705-a215-4c47-8620-d0f45bbb84f8'::uuid THEN
     is_admin := TRUE;
  END IF;

  IF NOT is_admin THEN
    RAISE EXCEPTION 'Access Denied V4. User: %', current_uid;
  END IF;

  -- 3. Execute Update (Fixing Type Mismatch)
  UPDATE public.image_slots
  SET 
    last_image_base64 = NULL,
    history_urls = '[]'::jsonb, -- FIX: Assign JSONB empty array, NOT text array
    is_locked = TRUE,
    updated_at = NOW()
  WHERE id = slot_id;
  
END;
$$;


ALTER FUNCTION "public"."admin_clear_slot"("slot_id" "uuid") OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."handle_new_user"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
BEGIN
  INSERT INTO public.profiles (id, full_name, role, access_code)
  VALUES (
    new.id, 
    new.raw_user_meta_data->>'full_name', 
    COALESCE(new.raw_user_meta_data->>'role', 'student'),
    new.raw_user_meta_data->>'access_code'
  );
  RETURN new;
END;
$$;


